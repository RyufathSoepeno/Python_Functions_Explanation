def increment(number, by):
    return number + by

result = increment(5, 4)
print(result)
# OR
print(increment(5, 4))
# OR
print(increment(number=5, by=4))

# number= and by= are keyword arguments

# With this you do not need to worry if the answer is 9 or 54
# This IDE doesnt combine two numbers with +, they add them

# INCREMENT IS NOT A FUNCTION, BUT A FUNCTION_NAME

